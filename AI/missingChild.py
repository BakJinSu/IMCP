from sklearn.cluster import KMeans
import numpy as np
import pandas as pd
import seaborn as sb
import matplotlib.pyplot as plt

df = pd.read_excel('.\CordData.xlsx')

#kmeans
sb.lmplot('latitude', 'longitude', data=df, fit_reg=False, scatter_kws={"s": 100})
points = df.values
kmeans = KMeans(n_clusters=6).fit(points)
print(kmeans.cluster_centers_)

df['cluster'] = kmeans.labels_
print(df)

sb.lmplot('latitude', 'longitude', data=df, fit_reg=False, scatter_kws={"s": 150}, hue="cluster")
points = df.values

plt.title('K-means')
plt.xlabel('latitude')
plt.ylabel('longitude')
plt.show()

